#include <math.h>
 float S, V;
 int main() {
    float r, h; //введення змінних
    printf("Задайте радіус основи циліндра r="); //рядок для введення цілого числа r
    scanf("%f",&r); //cканування цілого числа
    printf("Задайте висоту циліндра h="); //рядок для введення цілого числа h
    scanf("%f",&h); //cканування цілого числа
    V=M_PI*pow(r,2)*h; //формула об'єму
    S=2*M_PI*r*h; //формула площі повної поверхні
    printf("Площа бічної поверхні циліндра=%.2lf \n", S); //виведення чисел з функцій
    printf("Об'єм циліндра=%.2lf \n", V);
   return 0;
     }
