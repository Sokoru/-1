#include <stdio.h>
int main() {
    int a, b, c;
    printf("Задайте першу сторону відрізка: ");
    scanf("%d",&a);
    printf("Задайте другу сторону відрізка: ");
    scanf("%d",&b);
    printf("Задайте третю сторону відрізка: ");
    scanf("%d",&c);
    if ((a + b > c) && (a + c > b) && (b + c > a)) {
        printf("З цих відрізків можна побудувати трикутник");
    } else {
        printf("З цих відрізків не можна побудувати трикутник");
    }
    return 0;
}